package day02;

public class F_StringEx01 {

	public static void main(String[] args) {
		// alt + shift + r 변수 일괄변경
		String str1 = 1+"hi2";
		String str2 = "1hi"+2;
		System.out.println(str1);
		System.out.println(str2);
		
		String str3 = 1+2+"3";
		String str4 = "1" + 2 + 3;
		
		System.out.println(str3);
		System.out.println(str4);
		
		

	}

}
