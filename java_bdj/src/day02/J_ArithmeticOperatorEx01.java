package day02;

public class J_ArithmeticOperatorEx01 {

	public static void main(String[] args) {
		
		int num1 = 5, num2 = 3;
		System.out.println("더하기 " + num1 + '+' + num2 + '=' + (num1+num2));
		System.out.println("빼애기 " + num1 + '-' + num2 + '=' + (num1-num2));
		System.out.println("곱하기 " + num1 + '*' + num2 + '=' + (num1*num2));
		System.out.println("나누기 " + num1 + '/' + num2 + '=' + ((double)num1/num2));
		System.out.println("나머지 " + num1 + '%' + num2 + '=' + (num1%num2));
		
	}

}
